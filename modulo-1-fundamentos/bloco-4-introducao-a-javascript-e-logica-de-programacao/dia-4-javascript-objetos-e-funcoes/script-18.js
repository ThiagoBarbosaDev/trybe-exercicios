function fruitSorter(fruitArray) {
  const fruitCart = {};
  for (fruit of fruitArray) {
    if (!fruitCart[fruit]) {
      fruitCart[fruit] = 0;
    }
    fruitCart[fruit] += 1;
  }

  let message = [`Sua cesta possui: `];

  for (item in fruitCart) {
    let qtd = fruitCart[item];

    qtd > 1
      ? message.push(`${qtd} ${item}s ,`)
      : message.push(`${qtd} ${item} ,`);
  }
  return `${message.slice(0, -1).join(" ")} e ${message
    .slice(-1)
    .toString()
    .slice(0, -2)}.`;
}

